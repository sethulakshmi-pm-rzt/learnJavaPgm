// Example for 'if, else if condition'
public class exampleIf {
    public static void main(String[] args) {
        int x = 100;
        int y = 20;
        if (x < y) {
            System.out.println("X less than Y");
        } else if (x == y) {
            System.out.println("X equal to Y");
        } else if (y < x) {
            System.out.println("Y less than X");
        }
    }
}
